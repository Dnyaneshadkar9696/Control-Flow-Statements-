
public class EvenNum200 {
	
	public static void main(String[] args) {
		
		// Print all even numbers between 1 to 200 using for loop.
		System.out.println("The even numbers between 1 to 200 is as follows: ");
		for(int i = 1; i <= 200; i++) {
			
			if(i%2 == 0) {
				System.out.println(i);
			}
		}
		
		
	
		
	}

}

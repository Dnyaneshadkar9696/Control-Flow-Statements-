
public class WhileIoop {
	
	public static void main(String[] args) {
		// here we will use the while loop
		
		// to print the table of 12 using while loop
		
		// in while loop we compulasory need the condition
		// The while loop and do while loop are quite similar but the 
		// main difference is that in while loop the condition is checked first and then the loop executes where as in do while the loop executes once.
		int i = 1; // Initialization
		System.out.println("The table of twelve is as follows: ");
		while(i<=10) {
			// condition and the body.
			System.out.println(12*i);
			i++; // Updation
		}
	}

}


public class DowhileLoop {
	
	public static void main(String[] args) {
		// here we will use do while loop to print numbers from 1 to 10
		int i = 1; // Initialization
		System.out.println("The numbers from 1 to 10 are as follows: ");
		do {
			System.out.println(i);
			i++; // Updation
			// we have to menstion inistiazation, condition and updation in every loop just the way or the syntax to do so is different.
			}while(i<=10); // Condition
	}

}
